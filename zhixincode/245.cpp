using namespace std;
typedef long long ll;
const int N = 1e5 + 10;

struct FastIO {
    static const int S = 1e7;
    int wpos;
    char wbuf[S];
    FastIO() : wpos(0) {}
    inline int xchar() {
        static char buf[S];
        static int len = 0, pos = 0;
        if (pos == len)
            pos = 0, len = fread(buf, 1, S, stdin);
        if (pos == len) exit(0);
        return buf[pos++];
    }
    inline int operator () () {
        int c = xchar(), x = 0;
        while (c <= 32) c = xchar();
        for (; '0' <= c && c <= '9'; c = xchar()) x = x * 10 + c - '0';
        return x;
    }
    inline ll operator ! () {
        int c = xchar(); ll x = 0;
        while (c <= 32) c = xchar();
        for (; '0' <= c && c <= '9'; c = xchar()) x = x * 10 + c - '0';
        return x;
    }
    inline void wchar(int x) {
        if (wpos == S) fwrite(wbuf, 1, S, stdout), wpos = 0;
        wbuf[wpos++] = x;
    }
    inline void operator () (ll x) {
        if (x < 0) wchar('-'), x = -x;
        char s[24];
        int n = 0;
        while (x || !n) s[n++] = '0' + x % 10, x /= 10;
        while (n--) wchar(s[n]);
        wchar('\n');
    }
    inline void space(ll x) {
        if (x < 0) wchar('-'), x = -x;
        char s[24];
        int n = 0;
        while (x || !n) s[n++] = '0' + x % 10, x /= 10;
        while (n--) wchar(s[n]);
        wchar(' ');
    }
    inline void nextline() {
        wchar('\n');
    }
    ~FastIO()
    {
        if (wpos) fwrite(wbuf, 1, wpos, stdout), wpos = 0;
    }
} io;


int n, a[N], b[N];
int id[N]; int x;

bool cmp(int i, int j) {
    return a[i] + (ll) b[j] * x > a[j] + (ll) b[i] * x;
}

ll check(int x) {
    :: x = x;

    //if(!is_sorted(id + 1, id + 1 + n, cmp) || 1) {
        sort(id + 1, id + 1 + n, cmp);
    //}
    ll sum = 0;
    for(int i = 1 ; i <= n ; ++ i) {
        sum += i <= x ? a[id[i]] : (ll) x * b[id[i]];
    }
    return sum;
}

int main() {
    n = io();
    for(int i = 1 ; i <= n ; ++ i) a[i] = io(), b[i] = io(), id[i] = i;
    int l = 0, r = n;
    while(r - l >= 20) {
        int len = (r - l) / 3;
        int m1 = l + len, m2 = r - len;
        ll x1 = check(m1), x2 = check(m2);
        if(x1 >= x2) {
            r = m2;
        } else {
            l = m1;
        }
    }
    ll ans = 0;
    for(int i = l ; i <= r ; ++ i)
        ans = max(ans, check(i));
    io(ans);
}
